
package Introduccion_POO;
public class Buzon {
    private int nro;
    private int cantidad;

    public Buzon(int nro, int cantidad) {
        this.nro = nro;
        this.cantidad = cantidad;
    }

    public int getNro() {
        return nro;
    }

    public int getCantidad() {
        return cantidad;
    }
}